# Tipo de dado BOOL (Boolean)
# Ao questionar algo em um programa, só existem duas respostas possíveis:
# Sim (TRUE)  ou nÃO (FALSE).
# Existem vários operadore para "questionar".
# Dentre eles 0 ==, que é um operador lógico que
# quationa se um valor é igual a outro

print(10 == 10)
print(11 == 10)
print(type(8 == 3))
print(type(10 == 10))