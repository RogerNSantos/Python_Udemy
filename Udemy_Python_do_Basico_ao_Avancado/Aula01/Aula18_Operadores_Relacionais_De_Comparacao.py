"""
Operadores de comparação (relacionais)
OP      Significado         Exemplo (True)
>       maior               2 > 1
>=      maior ou igual      2 >= 2
<       menor               1 < 2
<=      menor ou igual      2 <= 2
==      igual               'a' == 'a'
!=      diferente           'a' != 'b'
"""
maior = 2 > 1
maior_ou_igual = 2 >= 2
menor = 1 < 2
menor_ou_igual = 2 <= 2
igual = 'a' == 'a'
diferente = 'a' != 'b'
#print('Olha meu print aqui')
print(f'Dois é maior que um? {maior}')
print(f'Dois é maior ou igual a Dois? {maior_ou_igual}')
print(f'Um é menor que Dois? {menor}')
print(f'Dois é menor ou igual a Dois? {menor_ou_igual}')
print(f'Letra A é igual a letra A? {igual}')
print(f'Letra A é diferente de B? {diferente}')