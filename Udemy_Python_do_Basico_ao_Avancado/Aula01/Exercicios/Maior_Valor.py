primeiroNumero = int(input('Digite o primeiro valor: '))
segundoNumero = int(input('Digite o segundo valor: '))

if primeiroNumero >= segundoNumero:
    print(f'O primeiro número ({primeiroNumero}) é maior ou igual ao ({segundoNumero})')
else:
    print(f'O segundo número ({segundoNumero}) é maior que o primeiro número ({primeiroNumero})')  
  


